<template>
  <div class="mavonEditordev">
    <mavon-editor :class="{mavonEditor:toolbarsFlag}" v-model="returnVal" 
                  :toolbars='toolbars'
                  :toolbarsBackground='toolbarsBackground'
                  :subfield=subfield
                  :toolbarsFlag='toolbarsFlag'
                  defaultOpen="preview"
                  ref=md 
                  @imgAdd="$imgAdd" 
                  @imgDel="$imgDel"/>
  </div>
</template>

<script>
  import { uploadFile ,delFile } from '@/network/note'
  export default {
    name: 'MarkdownPlugin',
    props: {
      subfield: Boolean,
      toolbarsFlag: {
        type: Boolean,
        default(){
          return true
        }
      },
      article: {
        type: Object
      },
    },
    data() {
      return {
        returnVal: this.article.content,
        toolbarsBackground: '#f8f8f8',
        toolbars: {
          bold: true, // 粗体
          italic: true, // 斜体
          header: true, // 标题
          underline: true, // 下划线
          strikethrough: true, // 中划线
          mark: true, // 标记
          superscript: true, // 上角标
          subscript: true, // 下角标
          quote: true, // 引用
          ol: true, // 有序列表
          ul: true, // 无序列表
          link: true, // 链接
          imagelink: true, // 图片链接
          code: true, // code
          table: true, // 表格
          fullscreen: true, // 全屏编辑
          readmodel: true, // 沉浸式阅读
          htmlcode: true, // 展示html源码
          help: true, // 帮助
          /* 1.3.5 */
          undo: true, // 上一步
          redo: true, // 下一步
          trash: true, // 清空
          save: true, // 保存（触发events中的save事件）
          /* 1.4.2 */
          navigation: true, // 导航目录
          /* 2.1.8 */
          alignleft: true, // 左对齐
          aligncenter: true, // 居中
          alignright: true, // 右对齐
          /* 2.2.1 */
          subfield: true, // 单双栏模式
          preview: true, // 预览
      
        }
      }
    },
    methods: {
      // 绑定@imgAdd event
      $imgAdd(pos, $file){
      // 第一步.将图片上传到服务器.
        var formdata = new FormData();
        formdata.append('file', $file);
        uploadFile(formdata).then(res =>{
          if(res.status == 200){
            this.$refs.md.$img2Url(pos, res.obj.url);
            this.$message({
              message: '图片上传成功',
              type: 'success'
            });
          }else{
            this.$message.error('图片上传失败，请再次上传或者通知系统管理员');
          }
        
        })
      },
      $imgDel(pos){
        var formdata = new FormData();
        formdata.append('filePath', pos[0]);
        delFile(formdata).then(res =>{
          console.log(res);
          
        })
      }
    },
    components: {},
    watch: {
      article(newVal){
        console.log(newVal.content);
        
        this.returnVal = newVal.content
      }
    },
  }
</script>

<style scoped>
  .mavonEditor {
    height: 500px
  }
</style>
